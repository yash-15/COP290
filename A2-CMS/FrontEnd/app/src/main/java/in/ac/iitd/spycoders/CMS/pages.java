        switch(id)
        {
            case  R.id.nav_profile:
              intent=new Intent(getBaseContext(),MyProfile_activity.class);
              toast.setText("Your Profile");
              break;
          }
//            // Handle the camera action
//        } else if (id == R.id.nav_gallery) {
//
